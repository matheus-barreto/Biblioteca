    private void GravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GravarActionPerformed
        try {
            dv.Conexao();
            if (codigo.getText().equals("NOVO")) {
                dv.sttm = dv.con.prepareStatement("Insert into livro(data_aquisicao,edicao,nr_obra,id_editora) values(?,?,?,?)");
                dv.sttm.setString(1, dv.ConvData(data.getText()));
                dv.sttm.setString(2, edicao.getText());
                dv.sttm.setString(3, idObra[codObra.getSelectedIndex()]);
                dv.sttm.setString(4, idEditora[codEditora.getSelectedIndex()]);
            } else {
                dv.sttm = dv.con.prepareStatement("Update livro set data_aquisicao = ?, edicao = ?, nr_obra=?, id_editora=? Where nr_livro = ?");
                dv.sttm.setString(1, dv.ConvData(data.getText()));
                dv.sttm.setString(2, edicao.getText());
                dv.sttm.setString(3, idObra[codObra.getSelectedIndex()]);
                dv.sttm.setString(4, idEditora[codEditora.getSelectedIndex()]);
                dv.sttm.setString(5, codigo.getText());
            }
            dv.sttm.executeUpdate();
            dv.con.commit();
            JOptionPane.showMessageDialog(null, "Livro cadastrado com sucesso...");
            codigo.grabFocus();
        } catch (Exception e) {
            try {
                dv.con.rollback();
                e.printStackTrace();
                JOptionPane.showMessageDialog(null, "Erro no cadastro de Livro...");
            } catch (SQLException ex) {
            }
        } finally {
            try {
                dv.con.close();
            } catch (Exception ex) {
            }
        }
    }//GEN-LAST:event_GravarActionPerformed

    private void ApagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ApagarActionPerformed
        int resp = 0;
        try {
            dv.Conexao();
            resp = JOptionPane.showConfirmDialog(null, "Confirma Exclusão desse Livro ?");
            if (resp == 0) {
                dv.sttm = dv.con.prepareStatement("delete from livro where nr_livro = ?");
                dv.sttm.setString(1, codigo.getText());
                dv.sttm.executeUpdate();
            }
            dv.con.commit();
            codigo.grabFocus();
        } catch (Exception e) {
            try {
                dv.con.rollback();
                e.printStackTrace();
            } catch (SQLException ex) {
            }
        } finally {
            try {
                dv.con.close();
            } catch (Exception ex) {
            }
        }
    }//GEN-LAST:event_ApagarActionPerformed

