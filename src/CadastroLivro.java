    private void codigoFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_codigoFocusLost
        try {
            dv.Conexao();
            if (codigo.getText().equals("")) {
                codigo.setText("NOVO");
            } else {
                dv.sttm = dv.con.prepareStatement("Select L.data_aquisicao, L.edicao, O.titulo, E.nome_fantasia from livro L, obra O, Editora E  where (O.nr_obra = L.nr_obra) and (L.id_editora = E.id_editora) and L.nr_livro = ?");
                dv.sttm.setString(1, codigo.getText());
                ResultSet rs = dv.sttm.executeQuery();
                if (rs.next()) {
                    data.setText(dv.RetString(rs.getString("data_aquisicao"), "D"));
                    edicao.setText(rs.getString("edicao"));
                    codObra.setSelectedItem(rs.getString("titulo"));
                    codEditora.setSelectedItem(rs.getString("nome_fantasia"));
                } else {
                    codigo.setText("NOVO");
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        } finally {
            try {
                dv.con.close();
            } catch (Exception ex) {
            }
        }
    }//GEN-LAST:event_codigoFocusLost

