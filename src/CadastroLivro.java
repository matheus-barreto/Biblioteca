    public String[] SelecionarObra() {
        int indice = 0;
        String[] dados = null;
        try {
            dv.Conexao();
            ResultSet rsGenero = dv.stmt.executeQuery("Select nr_obra,titulo from obra order by titulo ");
            rsGenero.last();
            dados = new String[rsGenero.getRow()];
            idObra = new String[rsGenero.getRow()];
            rsGenero.beforeFirst();
            while (rsGenero.next()) {
                idObra[indice] = rsGenero.getString("nr_obra");
                dados[indice] = rsGenero.getString("titulo");
                indice++;
            }
        } catch (Exception e) {
            e.printStackTrace();
        } finally {
            try {
                dv.con.close();
            } catch (Exception ex) {
            }
        }
        return dados;
    }

