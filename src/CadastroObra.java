    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            dv.Conexao();
            if (codigo.getText().equals("NOVO")) {
                dv.sttm = dv.con.prepareStatement("Insert into obra(titulo,id_genero) values(?,?)");
                dv.sttm.setString(1, titulo.getText());
                dv.sttm.setString(2, idGenero[codGenero.getSelectedIndex()]);
            } else {
                dv.sttm = dv.con.prepareStatement("Update obra set titulo = ?, id_genero = ? Where nr_obra = ?");
                dv.sttm.setString(1, titulo.getText());
                dv.sttm.setString(2, idGenero[codGenero.getSelectedIndex()]);
                dv.sttm.setString(3, codigo.getText());
            }
            dv.sttm.executeUpdate();
            dv.con.commit();
            JOptionPane.showMessageDialog(null, "Obra cadastrado com sucesso...");
            codigo.grabFocus();
        } catch (Exception e) {
            try {
                dv.con.rollback();
                e.printStackTrace();
                JOptionPane.showMessageDialog(null, "Erro no cadastro de Obra...");
            } catch (SQLException ex) {
            }
        } finally {
            try {
                dv.con.close();
            } catch (Exception ex) {
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int resp = 0;
        try {
            dv.Conexao();
            resp = JOptionPane.showConfirmDialog(null, "Confirma Exclusão desse Obra ?");
            if (resp == 0) {
                dv.sttm = dv.con.prepareStatement("delete from obra where nr_obra = ?");
                dv.sttm.setString(1, codigo.getText());
                dv.sttm.executeUpdate();
            }
            dv.con.commit();
            codigo.grabFocus();
        } catch (Exception e) {
            try {
                dv.con.rollback();
                e.printStackTrace();
            } catch (SQLException ex) {
            }
        } finally {
            try {
                dv.con.close();
            } catch (Exception ex) {
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

