    public String[] SelecionarGenero() {
        int indice = 0;
        String[] dados = null;
        try {
            dv.Conexao();
            dv.sttm = dv.con.prepareStatement("Select id_genero,descricao from genero order by descricao");
            ResultSet rsGenero = dv.sttm.executeQuery();
            rsGenero.last();
            dados = new String[rsGenero.getRow()];
            idGenero = new String[rsGenero.getRow()];
            rsGenero.beforeFirst();
            while (rsGenero.next()) {
                idGenero[indice] = rsGenero.getString("id_genero");
                dados[indice] = rsGenero.getString("descricao");
                indice++;
            }
        } catch (Exception e) {
            e.printStackTrace();
        } finally {
            try {
                dv.con.close();
            } catch (Exception ex) {
            }
        }
        return dados;
    }

