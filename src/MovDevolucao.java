    private void DevolverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DevolverActionPerformed

        int linha = tabDevolucao.getSelectedRow();
        if (linha == -1) {
            JOptionPane.showMessageDialog(null, "Para devolver um livro primeiro digite um código e escolha o livro que queseja devolver na tabela!");
        } else {
            int resp = 0;
            try {
                dv.Conexao();
                resp = JOptionPane.showConfirmDialog(null, "Confirma Devolução dessa Obra?");
                if (resp == 0) {
                    dv.sttm = dv.con.prepareStatement("update movimentacao set data_devolucao = ? where matricula = ? and nr_livro = ?");
                    dv.sttm.setString(1, dv.ConvData(dv.DataSistema()));
                    dv.sttm.setString(2, codigoUsuario.getText());
                    dv.sttm.setString(3, tabModel.getValueAt(linha, 0).toString());
                    dv.sttm.executeUpdate();
                }
                dv.con.commit();
            } catch (Exception e) {
                try {
                    dv.con.rollback();
                    e.printStackTrace();
                } catch (SQLException ex) {
                }
            } finally {
                try {
                    dv.con.close();
                } catch (Exception ex) {
                }
            }
        }
        SelecionarDevolucao();

    }//GEN-LAST:event_DevolverActionPerformed

