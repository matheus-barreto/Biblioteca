    public void SelecionarDevolucao() {
        tabModel.setRowCount(0);
        try {
            dv.Conexao();
            dv.sttm = dv.con.prepareStatement("Select M.data_devolucao, M.matricula,L.nr_livro,O.titulo,M.data_emprestimo,M.data_prevista from livro L, obra O, movimentacao M Where (L.nr_obra = O.nr_obra) and (M.nr_livro = L.nr_livro) and M.matricula=? and m.data_devolucao IS NULL order by M.data_emprestimo, O.titulo");
            dv.sttm.setString(1, codigoUsuario.getText());

            ResultSet rs = dv.sttm.executeQuery();
            while (rs.next()) {
                String[] linha = new String[]{rs.getString("nr_livro"), rs.getString("titulo"), dv.RetString(rs.getString("data_emprestimo"),"D"), dv.RetString(rs.getString("data_prevista"),"D")};
                tabModel.addRow(linha);
            }
        } catch (Exception e) {
            e.printStackTrace();
        } finally {
            try {
                dv.con.close();
            } catch (Exception ex) {
            }
        }
    }

