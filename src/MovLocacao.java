    private void codigoUsuarioFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_codigoUsuarioFocusLost
        try {
            dv.Conexao();
            dv.sttm = dv.con.prepareStatement("Select nome from usuario where matricula = ?");
            dv.sttm.setString(1, codigoUsuario.getText());
            ResultSet rs = dv.sttm.executeQuery();
            if (rs.next()) {
                nome.setText(dv.RetString(rs.getString("nome"), "C"));
                SelecionarLocacao();
            }
        } catch (Exception e) {
            e.printStackTrace();
        } finally {
            try {
                dv.con.close();
            } catch (Exception ex) {
            }
        }
    }//GEN-LAST:event_codigoUsuarioFocusLost

