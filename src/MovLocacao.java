    private void codigoLivroFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_codigoLivroFocusLost
        try {
            dv.Conexao();
            dv.sttm = dv.con.prepareStatement("Select O.titulo, L.edicao  from obra O, livro L  Where (O.nr_obra = L.nr_obra) and L.nr_livro = ?");
            dv.sttm.setString(1, codigoLivro.getText());
            ResultSet rs = dv.sttm.executeQuery();
            if (rs.next()) {
                livro.setText(dv.RetString(rs.getString("titulo") + ", " + rs.getString("edicao"), "C"));
            }
        } catch (Exception e) {
            e.printStackTrace();
        } finally {
            try {
                dv.con.close();
            } catch (Exception ex) {
            }
        }

    }//GEN-LAST:event_codigoLivroFocusLost

    private void AdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AdicionarActionPerformed

        try {

            dv.Conexao();
            dv.sttm = dv.con.prepareStatement("Insert into movimentacao(matricula,nr_livro,data_emprestimo,data_prevista) values(?,?,?,?)");
            dv.sttm.setString(1, codigoUsuario.getText());
            dv.sttm.setString(2, codigoLivro.getText());
            dv.sttm.setString(3, dv.ConvData(data.getText()));
            dv.sttm.setString(4, dv.ConvData(devolucao.getText()));
            dv.sttm.executeUpdate();
            dv.con.commit();

        } catch (Exception e) {
            try {
                dv.con.rollback();
                e.printStackTrace();
            } catch (SQLException ex) {
            }
        } finally {
            try {
                dv.con.close();
            } catch (Exception ex) {
            }
        }
        
        SelecionarLocacao();
    }//GEN-LAST:event_AdicionarActionPerformed

    private void ExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ExcluirActionPerformed
        int linha = tabLocacao.getSelectedRow();
        int resp = 0;
        if (linha == -1) {
            JOptionPane.showMessageDialog(null, "Para excluir uma locação primeiro escolha a linha correspondente ao livro que queseja apagar na tabela!");
        } else {
            try {
                dv.Conexao();
                resp = JOptionPane.showConfirmDialog(null, "Confirma Exclusão dessa Locação?");
                if (resp == 0) {
                    dv.sttm = dv.con.prepareStatement("delete from movimentacao where nr_livro = ? and data_emprestimo = ?");
                    dv.sttm.setString(1, tabModel.getValueAt(linha, 0).toString());
                    dv.sttm.setString(2, dv.ConvData(tabModel.getValueAt(linha, 2).toString()));
                    dv.sttm.executeUpdate();
                }
                dv.con.commit();
            } catch (Exception e) {
                try {
                    dv.con.rollback();
                    e.printStackTrace();
                } catch (SQLException ex) {
                }
            } finally {
                try {
                    dv.con.close();
                } catch (Exception ex) {
                }
            }
            
            SelecionarLocacao();
        }
    }//GEN-LAST:event_ExcluirActionPerformed

